// Build the Windows installer: WirelessMulticamStudio-Setup.exe.
//
// Bundles the launcher exe + web assets + MediaMTX config + the native tools
// (mkcert, mediamtx, ffmpeg, ffprobe) into one Inno Setup installer that lays
// down a ready-to-run folder under the user's Documents, creates shortcuts +
// the writable runtime dirs, and offers the one-time HTTPS cert setup. Fully
// offline once built (no internet needed on the target).
//
//   node build/build-installer.mjs        # -> dist/WirelessMulticamStudio-Setup.exe
import innosetup from 'innosetup-compiler';
import pngToIco from 'png-to-ico';
import { execFileSync } from 'node:child_process';
import { writeFileSync, existsSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const DIST = join(ROOT, 'dist');
const EXE = join(DIST, 'multicam.exe');
const ICO = join(DIST, 'multicam.ico');
const ISS = join(DIST, 'installer.iss');
const APP_ID = '{{B3F1B8E2-7C4A-4E9D-9B1F-1A2C3D4E5F60}'; // stable -> upgrades/uninstall track
const VERSION = '0.1.2';

if (process.platform !== 'win32') {
  console.error('The installer build is Windows-only (Inno Setup).');
  process.exit(1);
}

mkdirSync(DIST, { recursive: true });

// Always rebuild the launcher so the installer ships the latest code.
console.log('Building multicam.exe...');
execFileSync(process.execPath, [join(ROOT, 'build', 'build-sea.mjs')], { stdio: 'inherit' });

const tools = ['mkcert.exe', 'mediamtx.exe', 'ffmpeg.exe', 'ffprobe.exe'];
for (const t of tools) {
  if (!existsSync(join(ROOT, 'tools', t))) throw new Error(`Missing tools/${t}. Run: npm run setup`);
}

console.log('Writing installer icon...');
writeFileSync(ICO, await pngToIco(join(ROOT, 'icon.png')));

const phonePwa = join(ROOT, 'phone-pwa');
const operatorDash = join(ROOT, 'operator-dashboard');
const mtxYml = join(ROOT, 'mediamtx', 'mediamtx.yml');
const toolsDir = join(ROOT, 'tools');

// Inno Setup script. Interpolated ${paths} carry real backslashes; the literal
// `\\*` / `\\tools` below render as single backslashes in the .iss.
const iss = `; Generated by build/build-installer.mjs
[Setup]
AppId=${APP_ID}
AppName=Wireless Multicam Studio
AppVersion=${VERSION}
AppPublisher=OpenIdle
DefaultDirName={userdocs}\\Wireless Multicam Studio
DisableProgramGroupPage=yes
PrivilegesRequired=lowest
OutputDir=${DIST}
OutputBaseFilename=WirelessMulticamStudio-Setup
Compression=lzma2/max
SolidCompression=yes
WizardStyle=modern
SetupIconFile=${ICO}
UninstallDisplayIcon={app}\\multicam.exe

[Files]
Source: "${EXE}"; DestDir: "{app}"; Flags: ignoreversion
Source: "${phonePwa}\\*"; DestDir: "{app}\\phone-pwa"; Flags: recursesubdirs ignoreversion
Source: "${operatorDash}\\*"; DestDir: "{app}\\operator-dashboard"; Flags: recursesubdirs ignoreversion
Source: "${mtxYml}"; DestDir: "{app}\\mediamtx"; Flags: ignoreversion
Source: "${toolsDir}\\mkcert.exe"; DestDir: "{app}\\tools"; Flags: ignoreversion
Source: "${toolsDir}\\mediamtx.exe"; DestDir: "{app}\\tools"; Flags: ignoreversion
Source: "${toolsDir}\\ffmpeg.exe"; DestDir: "{app}\\tools"; Flags: ignoreversion
Source: "${toolsDir}\\ffprobe.exe"; DestDir: "{app}\\tools"; Flags: ignoreversion
Source: "${join(ROOT, 'tray.ps1')}"; DestDir: "{app}"; Flags: ignoreversion
Source: "${ICO}"; DestDir: "{app}"; Flags: ignoreversion

[Dirs]
Name: "{app}\\certs"
Name: "{app}\\data"
Name: "{app}\\recordings"
Name: "{app}\\exports"
Name: "{app}\\logs"

[Tasks]
Name: "desktopicon"; Description: "Create a desktop shortcut"; GroupDescription: "Shortcuts:"

[Icons]
; Main shortcut launches the tray with NO console window. powershell.exe
; -WindowStyle Hidden no longer hides anything when Windows Terminal is the
; default terminal (Win11): WT owns the window and ignores the host's
; -WindowStyle. conhost --headless hosts the shell in a windowless console, so
; the tray runs truly hidden regardless of the user's default-terminal setting.
Name: "{autoprograms}\\Wireless Multicam Studio"; Filename: "{sys}\\conhost.exe"; Parameters: "--headless ""{sys}\\WindowsPowerShell\\v1.0\\powershell.exe"" -WindowStyle Hidden -ExecutionPolicy Bypass -File ""{app}\\tray.ps1"""; IconFilename: "{app}\\multicam.ico"; WorkingDir: "{app}"
Name: "{autoprograms}\\Wireless Multicam Studio (first-time HTTPS setup)"; Filename: "{app}\\multicam.exe"; Parameters: "certs"; IconFilename: "{app}\\multicam.ico"; WorkingDir: "{app}"
Name: "{autodesktop}\\Wireless Multicam Studio"; Filename: "{sys}\\conhost.exe"; Parameters: "--headless ""{sys}\\WindowsPowerShell\\v1.0\\powershell.exe"" -WindowStyle Hidden -ExecutionPolicy Bypass -File ""{app}\\tray.ps1"""; IconFilename: "{app}\\multicam.ico"; WorkingDir: "{app}"; Tasks: desktopicon

[Run]
Filename: "{app}\\multicam.exe"; Parameters: "certs"; WorkingDir: "{app}"; Description: "Set up the HTTPS certificate now (recommended; one-time, needs admin)"; Flags: postinstall skipifsilent runhidden
Filename: "{sys}\\conhost.exe"; Parameters: "--headless ""{sys}\\WindowsPowerShell\\v1.0\\powershell.exe"" -WindowStyle Hidden -ExecutionPolicy Bypass -File ""{app}\\tray.ps1"""; WorkingDir: "{app}"; Description: "Launch Wireless Multicam Studio now"; Flags: postinstall nowait skipifsilent unchecked

[Code]
{ A running studio holds locks on multicam.exe and the tools. Inno's Restart
  Manager can't end console-subsystem apps gracefully (no WM_QUERYENDSESSION
  pump), so on upgrade the user sees "applications using files" with a failing
  auto-close. Pre-empt it by running 'multicam down' here, before Inno scans. }
function PrepareToInstall(var NeedsRestart: Boolean): String;
var
  ResultCode: Integer;
  ExePath: String;
begin
  Result := '';
  ExePath := ExpandConstant('{app}\\multicam.exe');
  if FileExists(ExePath) then
  begin
    Exec(ExePath, 'down', ExpandConstant('{app}'), SW_HIDE, ewWaitUntilTerminated, ResultCode);
    Sleep(800);
  end;
end;
`;

writeFileSync(ISS, iss);
console.log('Compiling installer (packs ~0.5 GB of tools; this takes a while)...');
await new Promise((resolve, reject) => {
  innosetup(ISS, { gui: false, verbose: false }, (err) => (err ? reject(err) : resolve()));
});
console.log(`\nInstaller ready -> ${join(DIST, 'WirelessMulticamStudio-Setup.exe')}`);
process.exit(0);
